package org.example;

public class MainCalculator {
    public int Sum(int a, int b){
        return a + b;
    }
    public int Minus(int a, int b){
        return a - b;
    }
    public int Multi(int a, int b){
        return a * b;
    }
    public int Del(int a, int b){
        return a / b;
    }


}
